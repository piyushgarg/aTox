package ltd.evilcorp.core.tox.enums

/**
 * Перечисление сигналов управления активными мультимедийными вызовами ToxAV (Call Control).
 * Соответствует кодам `Toxav_Call_Control` в toxcore.
 */
enum class ToxavCallControl {
    /** Возобновить ранее приостановленный вызов. */
    RESUME,
    /** Приостановить (поставить на удержание) вызов. */
    PAUSE,
    /** Отклонить входящий вызов или завершить текущую сессию разговора. */
    CANCEL,
    /** Выключить микрофон (заглушить исходящий звук). */
    MUTE_AUDIO,
    /** Включить микрофон (возобновить отправку звука). */
    UNMUTE_AUDIO,
    /** Скрыть камеру (прекратить отправку собственного видеопотока). */
    HIDE_VIDEO,
    /** Показать камеру (возобновить трансляцию видео). */
    SHOW_VIDEO
}

package ltd.evilcorp.core.tox.enums

/**
 * Перечисление возможных битовых флагов состояний аудио- и видеозвонков.
 * Комбинируется в EnumSet на стороне Kotlin при получении битовой маски от нативного ядра.
 */
enum class ToxavFriendCallState {
    /** Произошла ошибка во время вызова. */
    Error,
    /** Звонок успешно завершен. */
    Finished,
    /** Мы отправляем исходящий аудиопоток (голос). */
    SendingAudio,
    /** Мы отправляем исходящий видеопоток с камеры. */
    SendingVideo,
    /** Собеседник транслирует нам свой звук (мы воспроизводим). */
    ReceivingAudio,
    /** Собеседник транслирует нам свое видео (мы отображаем). */
    ReceivingVideo
}
